package in.co.rays.array;

public class OnedArray {
	public static void main(String[] args) {
		
	
	String []arr=new String[5];
	arr[0]="lavish";
	arr[1]="arun";
	arr[2]="prashant";
	System.out.println(arr[0]);//to print a name at a index value
	for (int i = 0; i < arr.length; i++) {
		System.out.println(arr[i]);// to print all the value given in the array of strings 
		
	}
	
	
}
}